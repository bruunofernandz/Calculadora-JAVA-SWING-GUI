    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:     
        
        Regex regex = new Regex();
        
        if(regex.checkValor(txtValor1.getText()) == true || regex.checkValor(txtValor2.getText()) == true
           || txtValor1.getText().isEmpty() || txtValor2.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Por favor digite somente numeros e os campos não podem estar vazios !!! ");
        }else {
         calc.setValor1(Float.parseFloat(txtValor1.getText()));
        calc.setValor2(Float.parseFloat(txtValor2.getText()));
        

            if(option.getSelectedItem().equals("Adição"))
            {
                resultado.setText(String.valueOf(calc.somar()));
            }else if (option.getSelectedItem().equals("Subtração"))
            {
                resultado.setText(String.valueOf(calc.subtrair()));
            }else if(option.getSelectedItem().equals("Multiplicação"))
            {
                resultado.setText(String.valueOf(calc.multiplicar()));
            }else if(option.getSelectedItem().equals("Divisão"))
            {
                resultado.setText(String.valueOf(calc.dividir()));
            }else {
                JOptionPane.showMessageDialog(this, "Por favor, Selecione uma opção");
            }   
        }
       
    }//GEN-LAST:event_jButton1ActionPerformed

